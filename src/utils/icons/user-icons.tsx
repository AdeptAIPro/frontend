const createIcon = (path: JSX.Element) => {
  return ({ size = 24, color = 'currentColor', strokeWidth = 2, ...rest }: {
    size?: number | string;
    color?: string;
    strokeWidth?: number;
    [key: string]: any;
  }) => {
    return (
      <svg
        xmlns="http://www.w3.org/2000/svg"
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill="none"
        stroke={color}
        strokeWidth={strokeWidth}
        strokeLinecap="round"
        strokeLinejoin="round"
        {...rest}
      >
        {path}
      </svg>
    );
  };
};

